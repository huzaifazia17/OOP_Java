public class Point {
    private double point_x;
    private double point_y;

    // Create constructor
    public Point(double x, double y) {
        point_x = x;
        point_y = y;
    }

    // Getters and Setters

    public double getX() {
        return point_x;
    }

    public void setX(double x) {
        point_x = x;
    }

    public double getY() {
        return point_y;
    }

    public void setY(double y) {
        point_x = y;
    }
}
